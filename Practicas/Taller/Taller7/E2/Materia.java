
public class Materia {

    private int clave;
    private String nombre;

    public void setClave(int clave) {
        this.clave = clave;
    }

    public void setNombre(String nombre) {
        this.nombre = nombre;
    }

    public int getClave() {
        return clave;
    }

    public String getNombre() {
        return nombre;
    }

}
